package com.variables;

public final class LocalVariable {

	public static void main(String[] args) {
		int num=10;//Local variable
		System.out.println("Local Variable of int: "+num);
		
		long num1=100;
		System.out.println("Local Variable of long: "+num1);
		
		char num2='A';
		System.out.println("Local Variable of char: "+num2);
		
		float num3=10.5f;
		System.out.println("Local Variable of long: "+num3);
		
		double num4=100;
		System.out.println("Local Variable of long: "+num4);
		
		byte num5=1;
		System.out.println("Local Variable of long: "+num5);
		
		String fn="TEJAS";
		String sn="N";
		int age=20;
		System.out.println("DETAILS:"+fn+" "+sn+" "+"Age: "+age);

	}

}
