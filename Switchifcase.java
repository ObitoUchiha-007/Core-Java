package SelectionStatments;

public class Switchifcase {

	public static void main(String[] args) {
		int x=10;
		switch(x) {
		case 10: System.out.println("Hello");
		
		
		case 20: System.out.println("Hi");
		
		default:
			System.out.println("World");
			
		case 21:
			System.out.println("Java");
			break;
		}

	}

}
