package Operators;

public class Shiftsops {
public static void main(String[] args) {
	int a=11;
	int b=a<<2;
	int c=a>>2;
	int d=a>>3;
	int e=a<<1;
	int f=a<<4;
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	System.out.println(f);
}
}